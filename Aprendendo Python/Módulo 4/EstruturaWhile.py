# A estrutura While (enquanto), executa um determinado conjunto de instruções, enquanto a condição verificada no início permanecer verdadeira.
# - Diferentemente da estrutura For, não é necessário determinar o número de vezes que a condição será executada

# Sintaxe da estrutura While:
# While condição {bloco de código}

x = 1;
while x <= 15:
    print(x);
    x = x+1

# OBS.: na estrutura While devemos inicializar a variável antes do início do laço (x = 1);
# Também deve-se realizar o incremento dentro do bloco de repetição (x = x+1;).

